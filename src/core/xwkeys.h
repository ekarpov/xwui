// NOTE: for some reason windows doesn't define virtual keys for numbers 
//       and alphabetic characters, so we need to define our own.
// https://msdn.microsoft.com/en-us/library/windows/desktop/dd375731(v=vs.85).aspx

// number keys
#define XW_VK_0                 0x30
#define XW_VK_1                 0x31
#define XW_VK_2                 0x32
#define XW_VK_3                 0x33
#define XW_VK_4                 0x34
#define XW_VK_5                 0x35
#define XW_VK_6                 0x36
#define XW_VK_7                 0x37
#define XW_VK_8                 0x38
#define XW_VK_9                 0x39

// alpha keys
#define XW_VK_A                 0x041
#define XW_VK_B                 0x042
#define XW_VK_C                 0x043
#define XW_VK_D                 0x044
#define XW_VK_E                 0x045
#define XW_VK_F                 0x046
#define XW_VK_G                 0x047
#define XW_VK_H                 0x048
#define XW_VK_I                 0x049
#define XW_VK_J                 0x04A
#define XW_VK_K                 0x04B
#define XW_VK_L                 0x04C
#define XW_VK_M                 0x04D
#define XW_VK_N                 0x04E
#define XW_VK_O                 0x04F
#define XW_VK_P                 0x050
#define XW_VK_Q                 0x051
#define XW_VK_R                 0x052
#define XW_VK_S                 0x053
#define XW_VK_T                 0x054
#define XW_VK_U                 0x055
#define XW_VK_V                 0x056
#define XW_VK_W                 0x057
#define XW_VK_X                 0x058
#define XW_VK_Y                 0x059
#define XW_VK_Z                 0x05A

/////////////////////////////////////////////////////////////////////

#endif // _XWKEYS_H_

